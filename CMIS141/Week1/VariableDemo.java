//Declare and initialize primitive variables
public class VariableDemo {

    public static void main (String[] args) {
        byte myByte = 20;
        short myShort = 2000;
        int myInt = 20000;
        long myLong = 2000000L;// L declare the long # in the program
        float myFloat = 32.345f;// the f declare the float # in the program
        double myDouble = 32.932;
        boolean myBoolean = true;
        char myChar = 'C';
        final double SPEEDOFSOUND = 343.6;

        // Print each variable along with the value
        // Final Double SPEEDOFSOUND

        System.out.println("my Byte: " + myByte);
        System.out.println("my Short: " + myShort);
        System.out.println("my Int: " + myInt);
        System.out.println("my Long: " + myLong);
        System.out.println("my Float: " + myFloat);
        System.out.println("my Double: " + myDouble);
        System.out.println("my Boolean: " + myBoolean);
        System.out.println("my Char: " + myChar);
        System.out.println("SPEEDOFSOUND: " + SPEEDOFSOUND);



    }
}